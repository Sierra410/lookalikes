func isRuneInSlice(r rune, s []rune) bool {
	for _, v := range s {
		if r == v {
			return true
		}
	}
	return false
}

func decodeRune(r rune) rune {
	for i, s := range alikes {
		if isRuneInSlice(r, s) {
			return proper[i]
		}
	}

	return r
}

func Decode(s string) string {
	builder := strings.Builder{}
	builder.Grow(len(s))

	for _, r := range s {
		builder.WriteRune(decodeRune(r))
	}

	return builder.String()
}
